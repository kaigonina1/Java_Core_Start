package start.lesson4;

public class Conditions {
    public static void main(String[] args) {
        int x = 5;
        int y = 6;

        if (x > y){
            System.out.println("X more than Y");
        } else if (x == y){
            System.out.println("X equals Y");
        } else {
            System.out.println("X less than Y");
        }
    }
}
